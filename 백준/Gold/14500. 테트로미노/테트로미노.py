def get(x,y):
    max_sum = 0

    for i in range(len(cases)):
        is_possible = True
        sum_of_nums = 0

        for dx in range(0,4):
            for dy in range(0,4):
                if cases[i][dx][dy] == 0:
                    continue
                if x + dx >= n or y + dy >= m:
                    is_possible = False
                else:
                    sum_of_nums += graph[x+dx][y+dy]
        if is_possible:
            max_sum = max(max_sum, sum_of_nums)
    return max_sum

